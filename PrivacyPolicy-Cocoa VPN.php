รวมถึงแต่ไม่จำกัดเพียงการรับประกันโดยปริยายของความสามารถในเชิงพาณิชย์
และความเหมาะสมสำหรับวัตถุประสงค์เฉพาะจะถูกปฏิเสธ ไม่ว่าในกรณีใด
