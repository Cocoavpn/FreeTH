ความเสียหายพิเศษ ความเสียหายที่เป็นแบบอย่างหรือเป็นผลสืบเนื่อง รวมถึงแต่ไม่จำกัด
การจัดซื้อจัดจ้างสินค้าหรือบริการทดแทน การสูญเสียการใช้งาน ข้อมูล หรือ
กำไร; หรือการหยุดชะงักของธุรกิจ อย่างไรก็ตาม เกิดจากทฤษฎีใด
